class Score:    

    witches_killed = 0
    witches_cured = 0
    magicians_vaccinated = 0
    magicians_remaining = 0
    magicians_infected = 0
    turns_taken = 0
    
    @staticmethod
    def reset_data():
        Score.witches_killed = 0
        Score.witches_cured = 0
        Score.magicians_vaccinated = 0
        Score.magicians_remaining = 0
        Score.magicians_infected = 0
        Score.turns_taken = 0




